    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Cart
        if(bpqnt.getText().isEmpty()){
        JOptionPane.showMessageDialog(this,"Quantity is Empty!!");
        bpqnt.requestFocus();
        }
        else{
        int rate=Integer.parseInt(bprate.getText());
        int qnty=Integer.parseInt(bpqnt.getText());
        int total=rate*qnty;
        DefaultTableModel dtm=(DefaultTableModel)jTable1.getModel();
        dtm.addRow(new Object[]{bpname.getText(),bpdesc.getText(),bprate.getText(),bpqnt.getText(),total});
        finalTotal=finalTotal+total;
        String totaladd=String.valueOf(finalTotal);
        fulltotal.setText(totaladd);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

