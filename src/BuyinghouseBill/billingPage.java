    private void bdnameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bdnameActionPerformed
        //BuyerName Search Action
        String bname=bdname.getText();
        try{
        Connection con=connection.getcon();
        Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select *from newbuyer where `BuyerName` like '"+bname+"%'");
            if(rs.next()){
            bdname.setText(rs.getString(1));
            bdphone.setText(rs.getString(2));
            bdemail.setText(rs.getString(3));
            bdaddress.setText(rs.getString(4));
            
            }
            else{
            bdphone.setText("");
            bdemail.setText("");
            bdaddress.setText("");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_bdnameActionPerformed

