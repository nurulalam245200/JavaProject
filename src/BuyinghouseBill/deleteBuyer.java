    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Delete Buyer
        int dc=JOptionPane.showConfirmDialog(this,"Do you want to delete!!","Select Any one",JOptionPane.YES_NO_CANCEL_OPTION);
    if(dc==0){
        String buyerphone=dbc.getText();
   
        try{
            Connection con=connection.getcon();
            Statement st=con.createStatement();
            int upb=st.executeUpdate("delete  from newbuyer where `BuyerPhone` ='"+buyerphone+"'");
           
            if(upb>0){
                JOptionPane.showMessageDialog(this, "Buyer Document Delete Sucesessfully!!!");
                setVisible(false);
                new deleteBuyer().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(this, "Buyer data Not Delete");}
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }}

    }//GEN-LAST:event_jButton1ActionPerformed

