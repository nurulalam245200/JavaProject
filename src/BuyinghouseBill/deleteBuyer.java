    private void searchbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchbuttonActionPerformed
        //Search
        String search=searchtf.getText();
        try{
            Connection con=connection.getcon();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select *from newbuyer where BuyerPhone like '"+search+"%'");
            if(rs.next()){
                dbn.setText(rs.getString(1));
                dbc.setText(rs.getString(2));
                dbe.setText(rs.getString(3));
                dbadd.setText(rs.getString(4));
                dbgen.setSelectedItem(rs.getString(5));}
            else{

                JOptionPane.showMessageDialog(this, "Not Found Here Buyer");}

        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Buyer Is Not Found Here","Please Give Right Contract",JOptionPane.WARNING_MESSAGE);}

    }//GEN-LAST:event_searchbuttonActionPerformed

