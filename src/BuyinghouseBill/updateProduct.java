    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String upsearch=prodid.getText();
        try{
        Connection con=connection.getcon();
        Statement st=con.createStatement();
        ResultSet rs=st.executeQuery("select *from newproduct where `ProductId`='"+upsearch+"'");
        if(rs.next()){
        produname.setText(rs.getString(2));
        produrate.setText(rs.getString(3));
        produdes.setText(rs.getString(4));
        activebox.setSelectedItem(rs.getString(5));
        }
        else{
        JOptionPane.showMessageDialog(this,"Not Found Product");}
        }
        catch(Exception e){
        JOptionPane.showMessageDialog(null,e);}
        
    }//GEN-LAST:event_jButton4ActionPerformed

