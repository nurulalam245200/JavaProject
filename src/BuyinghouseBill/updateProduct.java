    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Product Update
        String pid=prodid.getText();
        String pname=produname.getText();
        String prate=produrate.getText();
        String pdesp=produdes.getText();
        String pactive=activebox.getSelectedItem().toString();
        try{
            Connection con=connection.getcon();
            Statement st=con.createStatement();
            int proup=st.executeUpdate("update newproduct set `ProductName`='"+pname+"',`ProductPrice`='"+prate+"',`ProductDescription`='"+pdesp+"',`ProductActive`='"+pactive+"' where `ProductId`='"+pid+"'");
            if(proup>0){
                JOptionPane.showMessageDialog(this, "Product Update Sucessfully!!");
                setVisible(false);
                new updateProduct().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(this,"Data Entry Faild!!");
            }
        }

        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

